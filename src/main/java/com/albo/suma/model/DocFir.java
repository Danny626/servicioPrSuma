package com.albo.suma.model;

public class DocFir {
	public String id;
	public String usrFir;
	public long fecFir;
	public String tipDoc;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUsrFir() {
		return usrFir;
	}

	public void setUsrFir(String usrFir) {
		this.usrFir = usrFir;
	}

	public long getFecFir() {
		return fecFir;
	}

	public void setFecFir(long fecFir) {
		this.fecFir = fecFir;
	}

	public String getTipDoc() {
		return tipDoc;
	}

	public void setTipDoc(String tipDoc) {
		this.tipDoc = tipDoc;
	}
}