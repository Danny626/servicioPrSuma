package com.albo.suma.model;

public class Bit {
	public String nom;
	public String usu;
	public long fec;
	public int verDoc;

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getUsu() {
		return usu;
	}

	public void setUsu(String usu) {
		this.usu = usu;
	}

	public long getFec() {
		return fec;
	}

	public void setFec(long fec) {
		this.fec = fec;
	}

	public int getVerDoc() {
		return verDoc;
	}

	public void setVerDoc(int verDoc) {
		this.verDoc = verDoc;
	}
}
