package com.albo.suma.model.pre;

public class DomPre {
	public String calAve;
	public String num;
	public String barZon;
	public Pai pai;
	public Dep dep;
	public Ciu ciu;
	public String tel;
	public String fax;
	public String corEle;

	public String getCalAve() {
		return calAve;
	}

	public void setCalAve(String calAve) {
		this.calAve = calAve;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getBarZon() {
		return barZon;
	}

	public void setBarZon(String barZon) {
		this.barZon = barZon;
	}

	public Pai getPai() {
		return pai;
	}

	public void setPai(Pai pai) {
		this.pai = pai;
	}

	public Dep getDep() {
		return dep;
	}

	public void setDep(Dep dep) {
		this.dep = dep;
	}

	public Ciu getCiu() {
		return ciu;
	}

	public void setCiu(Ciu ciu) {
		this.ciu = ciu;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getCorEle() {
		return corEle;
	}

	public void setCorEle(String corEle) {
		this.corEle = corEle;
	}

}
