package com.albo.suma.model.pre;

public class DetIte {
	public String desMer;
	public int canBul;
	public int pesBru;

	public String getDesMer() {
		return desMer;
	}

	public void setDesMer(String desMer) {
		this.desMer = desMer;
	}

	public int getCanBul() {
		return canBul;
	}

	public void setCanBul(int canBul) {
		this.canBul = canBul;
	}

	public int getPesBru() {
		return pesBru;
	}

	public void setPesBru(int pesBru) {
		this.pesBru = pesBru;
	}

}
